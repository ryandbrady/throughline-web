'use strict';

// Converts a Figma MCP `get_metadata` dump into a Throughline Web design
// module (server/real-design.js). This is how a real Figma file becomes a
// navigable structure: the MCP is the "screen reader parser" — it hands us
// node IDs, names, types and the visible/hidden flag; this script reshapes
// that into the lightweight raw-node tree build-a11y-tree.js consumes.
//
// Usage: node tools/import-figma-metadata.js <metadata.txt> [fileName]
//
// Note: get_metadata does NOT include text content or fills, so TEXT nodes
// carry only their layer name. Use the Figma Bridge plugin (or a Claude
// enrichment pass) when real copy and alt text are needed.

const fs = require('fs');
const path = require('path');

const TAG_TO_TYPE = {
  canvas: 'CANVAS',
  frame: 'FRAME',
  group: 'GROUP',
  section: 'SECTION',
  text: 'TEXT',
  instance: 'INSTANCE',
  component: 'COMPONENT',
  'component-set': 'COMPONENT_SET',
  ellipse: 'ELLIPSE',
  rectangle: 'RECTANGLE',
  'rounded-rectangle': 'RECTANGLE',
  vector: 'VECTOR',
  line: 'LINE',
  star: 'STAR',
  polygon: 'POLYGON',
  'boolean-operation': 'BOOLEAN_OPERATION',
};

function typeForTag(tag) {
  return TAG_TO_TYPE[tag] || tag.toUpperCase().replace(/-/g, '_');
}

function decodeEntities(s) {
  return s
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&apos;/g, "'")
    .replace(/&amp;/g, '&');
}

function parseAttrs(str) {
  const attrs = {};
  const re = /([\w-]+)="([^"]*)"/g;
  let m;
  while ((m = re.exec(str))) attrs[m[1]] = m[2];
  return attrs;
}

// Stack-based parse of the (element-only, no text content) metadata XML.
function parseXml(xml) {
  const tagRe = /<(\/?)([a-zA-Z][\w-]*)((?:\s+[\w-]+="[^"]*")*)\s*(\/?)>/g;
  const root = { children: [] };
  const stack = [root];
  let m;
  while ((m = tagRe.exec(xml))) {
    const closing = m[1] === '/';
    if (closing) {
      if (stack.length > 1) stack.pop();
      continue;
    }
    const attrs = parseAttrs(m[3]);
    const node = {
      id: attrs.id,
      name: decodeEntities(attrs.name || ''),
      type: typeForTag(m[2]),
      visible: attrs.hidden !== 'true',
      children: [],
    };
    stack[stack.length - 1].children.push(node);
    if (m[4] !== '/') stack.push(node);
  }
  return root.children;
}

// Drop empty children arrays so the emitted module stays compact.
function prune(node) {
  if (node.children && node.children.length) {
    node.children.forEach(prune);
  } else {
    delete node.children;
  }
  return node;
}

function summarize(nodes, acc) {
  for (const n of nodes) {
    acc.total += 1;
    if (n.visible === false) acc.hidden += 1;
    acc.byType[n.type] = (acc.byType[n.type] || 0) + 1;
    if (n.children) summarize(n.children, acc);
  }
  return acc;
}

function main() {
  const inPath = process.argv[2];
  if (!inPath) {
    console.error('Usage: node tools/import-figma-metadata.js <metadata.txt> [fileName]');
    process.exit(1);
  }
  const fileName = process.argv[3] || 'Imported Figma file';

  const raw = JSON.parse(fs.readFileSync(inPath, 'utf8'));
  // The dump is a [{type,text}] array; keep only the entries that are XML.
  const xml = raw
    .filter((part) => part && typeof part.text === 'string' && part.text.trimStart().startsWith('<'))
    .map((part) => part.text)
    .join('\n');

  const pages = parseXml(xml).filter((n) => n.type === 'CANVAS');
  pages.forEach(prune);

  const doc = {
    id: '0:0',
    name: fileName,
    type: 'DOCUMENT',
    children: pages,
  };

  const outPath = path.join(__dirname, '..', 'server', 'real-design.js');
  const banner =
    '// Generated by tools/import-figma-metadata.js from a Figma MCP get_metadata dump.\n' +
    '// Source file: ' + fileName + '\n' +
    '// Edit the importer, not this file.\n\n';
  fs.writeFileSync(outPath, banner + 'module.exports = ' + JSON.stringify(doc, null, 2) + ';\n');

  const stats = summarize(pages, { total: 0, hidden: 0, byType: {} });
  console.log('Wrote ' + path.relative(path.join(__dirname, '..'), outPath));
  console.log('  pages:  ' + pages.length);
  console.log('  nodes:  ' + stats.total + ' (' + stats.hidden + ' hidden)');
  console.log('  byType: ' + JSON.stringify(stats.byType));
}

main();
